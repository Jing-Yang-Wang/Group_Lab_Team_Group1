    private void btnPayTuitionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPayTuitionActionPerformed
    //检查余额是否为0或负数
    if(studentProfile.getBalance() <= 0){
        JOptionPane.showMessageDialog(this, "No balance available to pay tuition.");
        return;
    }
    //弹出输入框让用户输入要支付的金额
    String input = JOptionPane.showInputDialog(this,"Enter amount to pay:");
    if(input == null || input.isEmpty())return;
    
    //先准备一个double变量,用于接收解析后的金额
    double payAmount = 0.0;
    try{
        payAmount = Double.parseDouble(input);//把输入的字符串转成数字
    }catch(NumberFormatException e){//转换失败就给出提示
        JOptionPane.showMessageDialog(this,"Please enter a valid number!");
        return;
    }
    
    //输入金额必须>0
    if(payAmount <= 0){
        JOptionPane.showMessageDialog(this,"Amount must be greater than 0!");
        return;
    }
    
    //检查余额是否足够支付
    if(studentProfile.getBalance() < payAmount){
        JOptionPane.showMessageDialog(this,"Insufficient Balance!");
        return;
    }
    
    //更新账户数据(余额/应缴/已缴)
    studentProfile.setBalance(studentProfile.getBalance()-payAmount);//余额=余额-支付额
    studentProfile.setPaidAmount(studentProfile.getPaidAmount()+payAmount);//已缴=已缴+支付额
    studentProfile.setOutstandingTuition(Math.max(0,studentProfile.getOutstandingTuition()-payAmount));//应缴=应缴-支付额（不可小于0）
    
    //新增一条缴费记录(日期,金额,备注类型,状态)
    studentProfile.addPaymentRecord(new PaymentRecord(new java.util.Date(), payAmount, "Tuition Payment", "Completed"));
    
    //刷新页面显示:上面三个文本框+下面的表格
    displayStudentFinancialInfo();//最新余额/应缴/已缴
    populatePaymentHistory();//最新记录填到JTabel
    
    //成功提示
    JOptionPane.showMessageDialog(this,"Payment successful!");
    
    
    
    }//GEN-LAST:event_btnPayTuitionActionPerformed

    private void btnRefundTuitionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefundTuitionActionPerformed
           //让用户输入要退款的金额
        String input = JOptionPane.showInputDialog(this,"Enter refund amount:");
        //取消或空直接
        if(input == null || input.isEmpty())return;
        
        //准备接收金额
        double refundAmount = 0.0;
        try{
            refundAmount = Double.parseDouble(input);//把字符串转成double
        }catch(NumberFormatException e){//非法输入提示
            JOptionPane.showMessageDialog(this,"Please enter a valid number!");
            return;
        }
    
        //金额必须>0
        if(refundAmount <= 0){
            JOptionPane.showMessageDialog(this,"Amount must be greater than 0!");
            return;
        }
        //退款不能超过已缴金额
        if(studentProfile.getPaidAmount()<refundAmount){
            JOptionPane.showMessageDialog(this,"No enough paid tuition to refund!");
            return;
        }
        //更新账户
        studentProfile.setBalance(studentProfile.getBalance()+refundAmount);
        studentProfile.setPaidAmount(studentProfile.getPaidAmount()-refundAmount);
        studentProfile.setOutstandingTuition(studentProfile.getOutstandingTuition()+refundAmount);
        //追加一条退款历史记录
        studentProfile.addPaymentRecord(new PaymentRecord(new java.util.Date(),-refundAmount, "Tuition Refund", "Refunded"));
        
        //刷新显示
        displayStudentFinancialInfo();
        populatePaymentHistory();
        
        //成功提示
        JOptionPane.showMessageDialog(this,"Refund successful!");
          
        
    }//GEN-LAST:event_btnRefundTuitionActionPerformed

    private void btnViewTranscriptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewTranscriptActionPerformed
        //如果学费未缴清→禁止查看成绩单
        if(studentProfile.getOutstandingTuition() >0){
            JOptionPane.showMessageDialog(this,"You can not view the transcript until all tuition is paid off.");
            return;
        }
        //学费已缴清→打开成绩单页面
        TranscriptReviewJPanel panel = new TranscriptReviewJPanel(userProcessContainer, studentProfile);
        userProcessContainer.add("TranscriptJPanel", panel);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
    }//GEN-LAST:event_btnViewTranscriptActionPerformed

