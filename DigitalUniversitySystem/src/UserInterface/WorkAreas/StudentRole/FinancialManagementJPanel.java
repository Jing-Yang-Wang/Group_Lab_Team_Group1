    //显示缴费历史表格
    private void populatePaymentHistory(){
    DefaultTableModel model = (DefaultTableModel)tblPaymentHistory.getModel();
    model.setRowCount(0);//清空表格
    
    //从studentProfile获取付款记录
    for(PaymentRecord record: studentProfile.getPaymentHistory()){
        Object[]row = new Object[4];
        row[0] = record.getDate();
        row[1] = record.getNote();
        row[2] = record.getAmount();
        row[3] = record.getStatus();
        model.addRow(row);
    }
    }
